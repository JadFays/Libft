#include "includes/libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	char *ptr_s1;
	char *ptr_s2;

	ptr_s1 = (char*)s1;
	ptr_s2 = (char*)s2;
	while (n-- > 0)
	{
		if (*ptr_s1 != *ptr_s2)
			return (*ptr_s1 - *ptr_s2);
		ptr_s1++;
		ptr_s2++;
	}
	return (0);
}

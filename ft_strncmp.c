#include "includes/libft.h"

int	ft_strncmp(const char *s1, const char *s2, unsigned int n)
{
	int i;

	i = 0;
	while (s1[i] == s2[i] && --n > 0)
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (s1[i] - s2[i]);
}

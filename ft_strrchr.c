#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*ptr;

	i = ft_strlen(s);
	ptr = (char *)s;
	while (i >= 0)
	{
		if (s[i] == c)
			return (&ptr[i]);
		i--;
	}
	return (NULL);
}
